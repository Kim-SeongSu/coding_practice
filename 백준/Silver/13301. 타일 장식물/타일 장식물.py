'''
1  1  4
2  1  6                     
3  2  10 = 2*3 + 1*2 + 1*2     = 2*4 + 1*2
4  3  16 = 3*3 + 2*2 + 1*2 + 1 = 3*4 + 2*2
5  5  26 = 5*3 + 3*2 + 2*2 + 1 = 5*4 + 3*2
6  8  42 = 8*3 + 5*2 + 3*2 + 2 = 8*4 + 5*2
'''

N = int(input())

if N < 2:
    print([0,4,6][N])
else:
    x = [0]*(N+1)
    x[1], x[2] = 4, 6

    for i in range(3,N+1):
        x[i] = x[i-1] + x[i-2]
    print(x[N])